#!/bin/bash

# BitsDraw Implementation Status Checker
# Verifies current implementation status against the complete plan

echo "ğŸ” BitsDraw Implementation Status Check"
echo "======================================="
echo ""

# Colors
GREEN='\033[0;32m'
RED='\033[0;31m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Counters
IMPLEMENTED=0
MISSING=0
TOTAL=0

check_file() {
    local file=$1
    local description=$2
    TOTAL=$((TOTAL + 1))
    
    if [ -f "$file" ]; then
        echo -e "  âœ… ${GREEN}$description${NC}"
        IMPLEMENTED=$((IMPLEMENTED + 1))
    else
        echo -e "  âŒ ${RED}$description${NC}"
        MISSING=$((MISSING + 1))
    fi
}

check_dir() {
    local dir=$1
    local description=$2
    TOTAL=$((TOTAL + 1))
    
    if [ -d "$dir" ]; then
        echo -e "  âœ… ${GREEN}$description${NC}"
        IMPLEMENTED=$((IMPLEMENTED + 1))
    else
        echo -e "  âŒ ${RED}$description${NC}"
        MISSING=$((MISSING + 1))
    fi
}

check_dependency() {
    local cmd=$1
    local description=$2
    
    if command -v "$cmd" &> /dev/null; then
        echo -e "  âœ… ${GREEN}$description${NC}"
    else
        echo -e "  âš ï¸  ${YELLOW}$description (optional)${NC}"
    fi
}

echo -e "${BLUE}ğŸ“‚ Project Structure:${NC}"
check_dir "docs" "GitHub Pages directory"
check_dir "docs/wasm" "WASM modules directory"
check_dir "docs/utils" "Enhanced utilities directory"
check_dir "wasm-src" "Rust source directory"
check_dir "tests" "Test suite directory"
check_dir ".github/workflows" "CI/CD workflows"

echo ""
echo -e "${BLUE}ğŸ¦€ WASM Implementation:${NC}"
check_file "wasm-src/Cargo.toml" "Rust project configuration"
check_file "wasm-src/src/lib.rs" "WASM core implementation"
check_file "docs/wasm/wasm-accelerator.js" "JavaScript WASM integration"

echo ""
echo -e "${BLUE}ğŸ¨ Alpha Layer System:${NC}"
check_file "docs/utils/canvas-unit.js" "Enhanced CanvasUnit with alpha support"
check_file "docs/utils/alpha-renderer.js" "Alpha-aware rendering system"

echo ""
echo -e "${BLUE}ğŸ—ï¸ Build & Deploy:${NC}"
check_file "build.sh" "Build automation script"
check_file ".github/workflows/deploy.yml" "GitHub Actions workflow"

echo ""
echo -e "${BLUE}ğŸ§ª Testing Framework:${NC}"
check_file "tests/test-runner.js" "Comprehensive test suite"

echo ""
echo -e "${BLUE}ğŸ“± PWA Features:${NC}"
if [ -f "docs/manifest.json" ]; then
    echo -e "  âœ… ${GREEN}PWA manifest (generated by build)${NC}"
    IMPLEMENTED=$((IMPLEMENTED + 1))
else
    echo -e "  ğŸ”„ ${YELLOW}PWA manifest (generated during build)${NC}"
fi
TOTAL=$((TOTAL + 1))

if [ -f "docs/sw.js" ]; then
    echo -e "  âœ… ${GREEN}Service worker (generated by build)${NC}"
    IMPLEMENTED=$((IMPLEMENTED + 1))
else
    echo -e "  ğŸ”„ ${YELLOW}Service worker (generated during build)${NC}"
fi
TOTAL=$((TOTAL + 1))

echo ""
echo -e "${BLUE}ğŸ”§ Dependencies:${NC}"
check_dependency "rustc" "Rust compiler"
check_dependency "wasm-pack" "WASM build tool"
check_dependency "node" "Node.js for testing"
check_dependency "git" "Git version control"

echo ""
echo -e "${BLUE}ğŸ“Š Implementation Summary:${NC}"
PERCENTAGE=$((IMPLEMENTED * 100 / TOTAL))

if [ $PERCENTAGE -eq 100 ]; then
    echo -e "  ğŸ‰ ${GREEN}Implementation Complete!${NC} ($IMPLEMENTED/$TOTAL)"
elif [ $PERCENTAGE -ge 80 ]; then
    echo -e "  ğŸš€ ${GREEN}Nearly Complete:${NC} $IMPLEMENTED/$TOTAL ($PERCENTAGE%)"
elif [ $PERCENTAGE -ge 60 ]; then
    echo -e "  ğŸ”„ ${YELLOW}Good Progress:${NC} $IMPLEMENTED/$TOTAL ($PERCENTAGE%)"
else
    echo -e "  ğŸ—ï¸  ${YELLOW}In Development:${NC} $IMPLEMENTED/$TOTAL ($PERCENTAGE%)"
fi

echo ""
echo -e "${BLUE}ğŸ“ Next Steps:${NC}"

if [ $MISSING -gt 0 ]; then
    echo "  1. Run ./build.sh to generate missing files"
    echo "  2. Install missing dependencies if needed"
    echo "  3. Run tests: node tests/test-runner.js"
else
    echo "  1. Build project: ./build.sh"
    echo "  2. Test locally: cd docs && python -m http.server 8000"
    echo "  3. Deploy to GitHub Pages"
fi

echo ""
echo -e "${BLUE}ğŸŒ Deployment Status:${NC}"

if [ -d "docs" ] && [ -f "build.sh" ]; then
    if [ -f "docs/index.html" ] && [ -f "docs/main.js" ]; then
        echo -e "  âœ… ${GREEN}Ready for GitHub Pages deployment${NC}"
    else
        echo -e "  ğŸ”„ ${YELLOW}Run build.sh to prepare deployment files${NC}"
    fi
else
    echo -e "  âŒ ${RED}Missing core deployment structure${NC}"
fi

echo ""
echo "ğŸ“– See README-IMPLEMENTATION.md for detailed instructions"
echo "ğŸ› Report issues at: https://github.com/[username]/BitsDraw/issues"